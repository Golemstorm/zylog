func Reverse(org string) string {
	var result strings.Builder
	b := strings.Split(org, "")
	for i := len(b) - 1; i >= 0; i-- {
		if v, ok := ReverseMap[b[i]]; ok {
			result.WriteString(v)
		} else {
			result.WriteString(b[i])
		}
	}
	return result.String()
}
